package JavaBasics;

public class VariableTypeDemo {
    public static void main(String[] args) {
        byte b = 8;
        System.out.println(b);
        char ch = 'a';
        System.out.println(ch);
        boolean var = true;
        System.out.println(var);
        float price = 10.5f;
        System.out.println(price);
        int number = 30;
        System.out.println(number);
        //long
        //double
        short n = 333;
        System.out.println(n);


    }
}
