package Operators;

public class AssignmentOperator {
    public static void main(String[] args) {
        int A=10;
      //  A=A+10;
        A+=10;
        int B=20;
        //B=B-20
        B-=20;
        int C=30;
        //C=C*30;
        C*=30;
        int D=40;
        //D=D/40;
        D/=40;
        int E=50;
        //E=E%50
        E%=50;
        System.out.println(A);
        System.out.println(B);
        System.out.println(C);
        System.out.println(D);
        System.out.println(E);

    }
}
