package Operators;

public class RelationalOperator {
    public static void main(String[] args) {
        int a=10;
        int b=10;
        System.out.println(a == b);
        System.out.println(a != b);
        int A=10;
        int B=5;
        System.out.println(A > B);
        System.out.println(A < B);
        System.out.println(a >= b);
        System.out.println(a <= b);

    }
}
